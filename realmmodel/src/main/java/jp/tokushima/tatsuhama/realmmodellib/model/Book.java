package jp.tokushima.tatsuhama.realmmodellib.model;

import io.realm.RealmObject;

public class Book extends RealmObject {
    private String name;
    private int price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
